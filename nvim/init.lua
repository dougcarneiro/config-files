-- Diagnostics
local diagnostic_status = true
local function toggle_diagnostic()
  diagnostic_status = not diagnostic_status
  if diagnostic_status then
    vim.diagnostic.show()
    print 'Displaying diagnostics'
  else
    vim.diagnostic.hide()
    print 'Hiding diagnostics'
  end
end

local virtual_text_status = false
vim.diagnostic.config { virtual_text = virtual_text_status } -- disables virtual_text at start up

local function toggle_diag_virtual_text()
  virtual_text_status = not virtual_text_status
  if not diagnostic_status then
    toggle_diagnostic()
    virtual_text_status = true
  end
  if virtual_text_status then
    print 'Showing diagnostics virtual text'
  else
    print 'Hiding diagnostics virtual text'
  end
  vim.diagnostic.config { virtual_text = virtual_text_status }
end

